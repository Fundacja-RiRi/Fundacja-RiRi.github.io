// Funkcja dodająca blob do kontenera
function createBlob({ svgIndex = 0, top, left, size = 20, opacity = 0.5 }) {
    const blob = document.createElement("div");
    blob.classList.add("blob");
    blob.style.width = size + "%";
    blob.style.opacity = opacity;

    if (top) blob.style.top = top;
    if (left) blob.style.left = left;

    blob.innerHTML = blobSVGs[svgIndex];
    container.appendChild(blob);
}

